namespace StatAnalyzer.Interfaces
{
    // Интерфейс для объектов, над которыми можно проводить статистический анализ.
    // Принцип разделения интерфейсов (ISP): каждый вариант реализует только нужное.

    public interface IAnalyzable
    {
        // Возвращает метку периода (год, день и т.д.) для отображения на оси X.
        string GetPeriodLabel();

        // Возвращает основное числовое значение для построения графика.
        double GetPrimaryValue();
    }
}
